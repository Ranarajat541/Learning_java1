package Switch_case;

public class Lab_Switch_int {
    public static void main(String[] args) {
        char code = 'c';
        switch (code){
            default:
                System.out.println("hello");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
